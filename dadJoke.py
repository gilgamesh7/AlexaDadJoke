class JokeIntentHandler(AbstractRequestHandler):
    def can_handle(self,handler_input):
        return ask_utils.is_intent_name("JokeIntent")(handler_input)
        
    def handle(self,handler_input):
        logger.info("In Joke Handler")
        jokeUrl="https://icanhazdadjoke.com/"
        jokeHeaders = {'Accept': 'application/json'}
        jokeResponse = requests.get(jokeUrl, headers=jokeHeaders)
        logger.info(f"Returned : {jokeResponse.status_code}")
        if (jokeResponse.status_code == 200):
            logger.info(jokeResponse)
            jokeResponseJson = jokeResponse.json()
            speak_output=jokeResponseJson["joke"]
        else:
            logger.info(jokeResponse)
            speak_output="Something went wrongwith the API call, see logs"
        
        return (handler_input.response_builder
            .speak(speak_output)
            .ask(speak_output)
            .response)

